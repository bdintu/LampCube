/* USER CODE BEGIN 0 */
typedef struct {
	float r;	/* R : PD12 */
	float g;	/* G : PD13 */
	float b;	/* B : PD14 */
} RGB;

/* USER CODE END 0 */

int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration----------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */
	RGB rgb = {1, 1, 1};

	volatile uint16_t ird_val;	/* Infrared Distance : PC4 */
	volatile uint16_t fsr_val;	/* Force Sensitive Resistor : PC5 */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_USART2_UART_Init();
  MX_ADC1_Init();
  MX_TIM4_Init();
  MX_ADC2_Init();

  /* USER CODE BEGIN 2 */
	HAL_ADCEx_Calibration_Start(&hadc1);
	HAL_ADC_Start(&hadc1);

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
  /* USER CODE END WHILE */

  /* USER CODE BEGIN 3 */
	while ( HAL_ADC_PollForConversion(&hadc1, 100) != HAL_OK ) {}
	ird_val = HAL_ADC_GetValue(&hadc1);
	USART_IR(ird_val);
		
	while ( HAL_ADC_PollForConversion(&hadc2, 100) != HAL_OK ) {}
	fsr_val = HAL_ADC_GetValue(&hadc2);
		
	htim4.Instance->CCR1 = (PERIOD-1)*rgb.r;
	htim4.Instance->CCR2 = (PERIOD-1)*rgb.g;
	htim4.Instance->CCR3 = (PERIOD-1)*rgb.b;
	HAL_TIM_PWM_Start(&htim4, TIM_CHANNEL_1);
	HAL_TIM_PWM_Start(&htim4, TIM_CHANNEL_2);
	HAL_TIM_PWM_Start(&htim4, TIM_CHANNEL_3);
	HAL_Delay(100);
	HAL_TIM_PWM_Stop(&htim4, TIM_CHANNEL_1);
	HAL_TIM_PWM_Stop(&htim4, TIM_CHANNEL_2);
	HAL_TIM_PWM_Stop(&htim4, TIM_CHANNEL_3);		
  }
  /* USER CODE END 3 */

}

