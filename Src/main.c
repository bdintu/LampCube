typedef struct {
	float r;	/* R : PA7 */
	float g;	/* G : PB0 */
	float b;	/* B : PB1 */
} RGB;

RGB color[] = {
	{0, 0, 0},	/* WHITE */
	{1, 0, 0},	/* RED */
	{0, 1, 0},	/* GREEN */
	{0, 0, 1}	/* BLUE */
};

uint16_t led_hex[] = {0x0000, 0x0100, 0x0300, 0x0700, 0x0f00, 0x1f00, 0x3f00, 0x7f00, 0xff00};

